//////////////////////////////////////////////////////////////////////////////////////////
//                                  SELECTION DE L'EEPROM                               //
//////////////////////////////////////////////////////////////////////////////////////////
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
extern void Eeprom_SelectChip(uint8_t EepromAddress, uint8_t EepromCapability);

//////////////////////////////////////////////////////////////////////////////////////////
//                                      LECTURE D'UN OCTET                              //
//////////////////////////////////////////////////////////////////////////////////////////
//----------------------------------------------------------------------------------------
// Retourne         : La valeur de l'octet lu
//----------------------------------------------------------------------------------------
