//                                    ECRIRE PLUSIEURS OCTETS                           //
//////////////////////////////////////////////////////////////////////////////////////////
//----------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------
// ArraySize        : Taille du tableau
// Retourne         : true ou false
//----------------------------------------------------------------------------------------
extern Boolean Eeprom_WriteBytesArray(uint16_t MemoryAddress, uint8_t *Array, uint8_t ArraySize);
